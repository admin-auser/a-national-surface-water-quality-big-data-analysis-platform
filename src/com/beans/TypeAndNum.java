package com.beans;

public class TypeAndNum {
	private String type;
	private int num;
	
	public TypeAndNum() {
		super();
	}
	public TypeAndNum(String type, int num) {
		super();
		this.type = type;
		this.num = num;
	}
	public String getType() {
		return type;
	}
	public int getNum() {
		return num;
	}
	public void setType(String type) {
		this.type = type;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
